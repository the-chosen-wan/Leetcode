int powmod(int a, int b, int m) {
    int res = 1;
    while (b > 0) {
        if (b & 1) {
            res = (res * 1ll * a) % m;
        }
        a = (a * 1ll * a) % m;
        b >>= 1;
    }
    return res;
}

int solve(int a, int b, int m) {
    a %= m, b %= m;
    int n = sqrt(m) + 1;
    map<int, int> vals;
    for (int p = 1; p <= n; ++p)
        vals[powmod(a, p * n, m)] = p;
    for (int q = 0; q <= n; ++q) {
        int cur = (powmod(a, q, m) * 1ll * b) % m;
        if (vals.count(cur)) {
            int ans = vals[cur] * n - q;
            return ans;
        }
    }
    return -1;
}

bool ans(int x,int y){
    int gcd = __gcd(x,y);

    if((gcd&(gcd-1))!=0)
        return false;
    
    x/=gcd;
    y/=gcd;

    if(x%2==0)
        swap(x,y);
    
    return solve(2,y,x)!=-1;
}

class Solution {
public:
    bool isReachable(int targetX, int targetY) {
        return ans(targetX,targetY);
    }
};