#include <iostream>
#include <bits/stdc++.h>
using namespace std;
using ll = long long;
using pii = pair<int, int>;
const int inf = ((1e9) + 7);
#define pb push_back
#define mp make_pair

string ans(string s1, string s2)
{
    int n1 = s1.size();
    int n2 = s2.size();
    int dp[n1][n2];
    memset(dp, inf, sizeof(dp));
}

class Solution
{
public:
    string shortestCommonSupersequence(string str1, string str2)
    {
        return ans(str1, str2);
    }
};